/* ─── Cambio de idioma ─── */
function setLang(lang) {
  document.documentElement.lang = lang;
  document.querySelectorAll('.lang-btn').forEach(b => b.classList.toggle('on', b.dataset.lang === lang));
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const v = T[lang][el.dataset.i18n];
    if (v !== undefined) el.textContent = v;
  });
  document.querySelectorAll('[data-i18n-h]').forEach(el => {
    const v = T[lang][el.dataset.i18nH];
    if (v !== undefined) el.innerHTML = v;
  });
  localStorage.setItem('lang', lang);
}

/* ─── WhatsApp tracking ─── */
function trackWA(category) {
  if (window.fbq) fbq('track', 'Contact', { content_category: category });
}

/* ─── Toggle formulario ─── */
function toggleForm() {
  const wrap = document.getElementById('emailForm');
  const btn = document.querySelector('.cta-email-toggle');
  const lang = document.documentElement.lang || 'es';
  if (wrap.hasAttribute('hidden')) {
    wrap.removeAttribute('hidden');
    btn.textContent = T[lang]['cta.email.close'];
  } else {
    wrap.setAttribute('hidden', '');
    btn.textContent = T[lang]['cta.email.toggle'];
  }
}

