/* ─── Intersection Observer ─── */
const obs = new IntersectionObserver(entries => {
  entries.forEach(e => { if (e.isIntersecting) { e.target.classList.add('in'); obs.unobserve(e.target); } });
}, { threshold: 0.12 });
document.querySelectorAll('.fu').forEach(el => obs.observe(el));

/* ─── Formulario ─── */
document.getElementById('cForm').addEventListener('submit', function(e) {
  e.preventDefault();
  let ok = true;
  this.querySelectorAll('[required]').forEach(f => {
    const err = !f.value.trim() || (f.type === 'email' && !f.value.includes('@'));
    f.style.borderColor = err ? 'var(--red)' : '';
    if (err) ok = false;
  });
  if (!ok) return;

  const nombre  = document.getElementById('f-nombre').value.trim();
  const empresa = document.getElementById('f-empresa').value.trim();
  const email   = document.getElementById('f-email').value.trim();
  document.getElementById('f-replyto').value = email;
  document.getElementById('f-subject').value =
    `Solicitud de presupuesto de ${nombre} (${empresa}) — Como la Seda`;

  const btn = this.querySelector('.fsub');
  const btnTxt = btn.textContent;
  btn.textContent = '...';
  btn.disabled = true;

  fetch('https://api.web3forms.com/submit', {
    method: 'POST',
    headers: { 'Accept': 'application/json' },
    body: new FormData(this)
  })
  .then(r => r.json())
  .then(() => {
    this.style.display = 'none';
    document.getElementById('fOk').classList.add('on');
    if (window.fbq) fbq('track', 'Lead');
  })
  .catch(() => {
    btn.textContent = btnTxt;
    btn.disabled = false;
  });
});
document.querySelectorAll('[required]').forEach(f => f.addEventListener('input', () => f.style.borderColor = ''));

/* ─── Smooth scroll ─── */
document.querySelectorAll('a[href^="#"]').forEach(a => {
  a.addEventListener('click', e => {
    const t = document.querySelector(a.getAttribute('href'));
    if (t) { e.preventDefault(); t.scrollIntoView({ behavior: 'smooth', block: 'start' }); }
  });
});

